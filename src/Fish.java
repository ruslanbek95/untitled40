public class Fish {
    private String name;
    private String dread;
    private  String eag;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDread() {
        return dread;
    }

    public void setDread(String dread) {
        this.dread = dread;
    }

    public String getEag() {
        return eag;
    }

    public void setEag(String eag) {
        this.eag = eag;
    }
}
